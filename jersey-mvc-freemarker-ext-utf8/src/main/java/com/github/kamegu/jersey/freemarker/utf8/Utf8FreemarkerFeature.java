package com.github.kamegu.jersey.freemarker.utf8;

import javax.ws.rs.ConstrainedTo;
import javax.ws.rs.RuntimeType;
import javax.ws.rs.core.Feature;
import javax.ws.rs.core.FeatureContext;
import org.glassfish.jersey.server.mvc.MvcFeature;

@ConstrainedTo(RuntimeType.SERVER)
public class Utf8FreemarkerFeature  implements Feature {
    @Override
    public boolean configure(final FeatureContext context) {
        final javax.ws.rs.core.Configuration config = context.getConfiguration();

        if (!config.isRegistered(Utf8FreemarkerProcessor.class)) {
            // Template Processor.
            context.register(Utf8FreemarkerProcessor.class);

            // MvcFeature.
            if (!config.isRegistered(MvcFeature.class)) {
                context.register(MvcFeature.class);
            }

            return true;
        }
        return false;
    }
}
